            LPergunta.setText("¡Gane!, ¿Quieres jugar de nuevo?");
            BtnJugar.setVisible(true);
            BtnJugar.setEnabled(true);
            BtnSi.setVisible(false);
            BtnSi.setEnabled(false);
            BtnNo.setVisible(false);
            BtnNo.setEnabled(false);
